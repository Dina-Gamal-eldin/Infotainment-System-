void HSTP_voidSendSynchrous(u16 Copy_u8DataToSend)
{
	s16 Local_s8Counter;
	u16 Local_u8Bit;
	for(Local_s8Counter =15 ; Local_s8Counter>=0;Local_s8Counter--)
	{
		/*Send bit by bit starting from MSB*/
		Local_u8Bit =GET_BIT(Copy_u8DataToSend,Local_s8Counter);
		MGPIO_voidWriteData(HSTP_SERIAL_DATA, Local_u8Bit);
		/*Send Pulse to Shift Clock*/
		MGPIO_voidWriteData(HSTP_SHIFT_CLOCK,1);
		TIM2_voidSetBusyWait(2);
		MGPIO_voidWriteData(HSTP_SHIFT_CLOCK,0);
		TIM2_voidSetBusyWait(2);



	}
	/*Send Pulse to Store Clock*/
	MGPIO_voidWriteData(HSTP_STORE_CLOCK,1);
	TIM2_voidSetBusyWait(2);
	MGPIO_voidWriteData(HSTP_STORE_CLOCK,0);
	TIM2_voidSetBusyWait(2);

}
